<template>
  <div v-if="Object.keys(detailInfo).length !== 0" class="goods-info">
    <div class="info-desc clear-fix">
      <div class="start"></div>
      <div class="desc">{{ detailInfo.desc }}</div>
      <div class="end"></div>
    </div>
    <div class="info-key">{{ detailInfo.detailImage[0].key }}</div>
    <div class="info-list">
      <!-- v-for遍历记得要动态绑定key作为唯一标识符，这样可以让v-for生成的东西有改变时效率更高
      因为vue的diff算法可以根据标识符直接找到对应位置进行更改，不用一项项比对，也不怕比对机制导致出错 -->
      <img
        v-for="(item, index) in detailInfo.detailImage[0].list"
        :key="index"
        :src="item"
        @load="imgLoad"
        alt=""
      />
    </div>
  </div>
</template>

<script>
export default {
  name: "DetailGoodsInfo",
  props: {
    detailInfo: {
      type: Object,
    },
  },
  data() {
    return {
      counter: 0,
      imagesLength: 0,
    };
  },
  methods: {
    imgLoad() {
      // 所有的图片都加载完了再进行一次回调就可以了
      // 这个++this.counter==等效于先++，再拿出来==
      if (++this.counter == this.imagesLength) {
        this.$emit("imageLoad");
        console.log("imageLoad");
      }
    },
  },
  // watch的用法是监听数据的改变，里面的数据只要改变，就会执行它对应的函数
  watch: {
    detailInfo() {
      // 获取图片的个数
      this.imagesLength = this.detailInfo.detailImage[0].list.length;
    },
  },
};
</script>

